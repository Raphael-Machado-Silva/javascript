 //----VETORES EM JAVASCRIPT (VARIAVEIS COMPOSTAS)----

let num = [5, 8, 2, 9, 3] 

num[5]= 0 // para adicionar mais um valor a variavel ou usar o:
num.push(7) // serve para adicionar mais um elemento, assim como o de cima
num.sort() // colocar em ordem crescente
num.length // esta tag serve para mostrar quantos elementos este vetor possui

console.log(`Nossa variável é: ${num}`)
console.log(`O meu elemento que ocupa a posição 1 é: ${num[1]}!`)
console.log(`Meu vetor possui: ${num.length} elementos!`)

//---------------OBS: Vetores usam [] e não {} ---------------- 

